/**
*
*  Multiply two 64-bit unsigned integers and produce 128-bit result
*
*  (u * v) -> w
*
*  Element w[0] of two-element array w always holds 64 least significant bits of result,
*  even on big endian machines.
*
*/
static inline void  multiply_128(
   ng_uint64_t   u,
   ng_uint64_t   v,
   ng_uint64_t*  w
)
{
   // Make use of 64x64->128 unsigned multiplication instruction
   // (if processor has this instruction and compiler allows for using it).
#if (defined(__GNUC__) || defined(__clang__)) && defined(__SIZEOF_INT128__) && (__SIZEOF_INT128__ == 16)
   unsigned __int128  value_w = ((unsigned __int128)u) * v;
   w[0] = ((ng_uint64_t)( value_w       ));
   w[1] = ((ng_uint64_t)( value_w >> 64 ));
#elif RTE_TOOLCHAIN_MSVC && defined(_M_X64)
   w[0] = _umul128( u, v, w + 1 );
#else
   // On other processors and compilers, do column multiplication.
   ng_uint64_t  u1 = u >> 32;   ng_uint64_t  u0 = u & UINT64_C(0xFFFFFFFF);
   ng_uint64_t  v1 = v >> 32;   ng_uint64_t  v0 = v & UINT64_C(0xFFFFFFFF);

   ng_uint64_t  t  = u0 * v0;
   ng_uint64_t  w0 = t & UINT64_C(0xFFFFFFFF);
   ng_uint64_t  k  = t >> 32;
   t            = u1 * v0 + k;

   ng_uint64_t  w1 = t & UINT64_C(0xFFFFFFFF);
   ng_uint64_t  w2 = t >> 32;
   t            = u0 * v1 + w1;
   k            = t >> 32;

   w[0] = (t << 32) + w0;
   w[1] = u1 * v1 + w2 + k;
#endif
}

/**
*
*  Count the number of leading zero bits in provided 64-bit unsigned integer
*
*  Behavior of this function is undefined if a == 0.
*
*/
static inline unsigned int  count_leading_zeros( ng_uint64_t  a )
{
  // Make use of 64-bit leading zero count instruction
  // (if processor has this instruction and compiler allows for using it).
#if defined(__GNUC__) || defined(__clang__)
  return  __builtin_clzll( a );
#elif RTE_TOOLCHAIN_MSVC && defined(_M_X64)
  unsigned long  most_significant_bit_number = 0;
  _BitScanReverse64( &most_significant_bit_number, a );
  return  63 - most_significant_bit_number;
#elif RTE_TOOLCHAIN_MSVC && (!defined(_M_X64))
  if( a <= UINT64_C(0xFFFFFFFF) )
  {
    unsigned long  most_significant_bit_number = 0;
    _BitScanReverse( &most_significant_bit_number, (ng_uint32_t)a );
    return  63 - most_significant_bit_number;
  }
  else
  {
    unsigned long  most_significant_bit_number = 0;
    _BitScanReverse( &most_significant_bit_number, (ng_uint32_t)(a >> 32) );
    return  31 - most_significant_bit_number;
  }
#else
  // On other processors and compilers, count leading zero bits manually
  unsigned int  n = 0;
  if( a <= UINT64_C(0x00000000FFFFFFFF) ) { n += 32; a <<= 32; }
  if( a <= UINT64_C(0x0000FFFFFFFFFFFF) ) { n += 16; a <<= 16; }
  if( a <= UINT64_C(0x00FFFFFFFFFFFFFF) ) { n += 8;  a <<= 8;  }
  if( a <= UINT64_C(0x0FFFFFFFFFFFFFFF) ) { n += 4;  a <<= 4;  }
  if( a <= UINT64_C(0x3FFFFFFFFFFFFFFF) ) { n += 2;  a <<= 2;  }
  if( a <= UINT64_C(0x7FFFFFFFFFFFFFFF) ) { n += 1;            }
  return  n;
#endif
}

/**
*
*  Convert extended-precision decimal to double-precision binary
*  (mantissa is rounded to nearest representable value)
*
*  (a * 10^b) -> (c * 2^d)
*
*  a != 0. Decimal point is on the right side of the input mantissa a.
*
*  Resulting binary mantissa c is rounded to 53 bits (as in IEEE 754 double-precision binary).
*  If remainder >= 0.5, then mantissa is rounded up, otherwise it's rounded down.
*
*  On exit from this function, binary point is located after first bit of mantissa,
*  which is always equal to 1 (binary exponent is adjusted accordingly).
*
*  @returns  1  Exited normally, no errors.
*            0  Invalid argument or internal error.
*
*/
static int  convert_extended_decimal_to_binary_and_round(
  ng_uint64_t   a,
  ng_int32_t    b,
  ng_uint64_t*  c,
  ng_int32_t*   d
)
{
  // 1. Check input arguments
  if(( a == 0 )||
  ( b < powers_of_ten_[0].decimal_exponent )||
  ( b > powers_of_ten_[sizeof(powers_of_ten_)/sizeof(powers_of_ten_[0]) - 1].decimal_exponent ))
   return  0;

  // 2. Convert (a * 10^b) -> (mantissa * 2^exponent)
  ng_uint64_t  mantissa = powers_of_ten_[ b - powers_of_ten_[0].decimal_exponent ].binary_mantissa;
  ng_int32_t   exponent = powers_of_ten_[ b - powers_of_ten_[0].decimal_exponent ].binary_exponent;
  ng_uint64_t  long_mantissa[2];
  multiply_128( a, mantissa, long_mantissa );
  if( long_mantissa[1] != 0 )   // high half
  {
    unsigned int  lz = count_leading_zeros( long_mantissa[1] );
    if( lz == 0 )
      mantissa = long_mantissa[1];
    else
      mantissa = ( long_mantissa[1] << lz )|( long_mantissa[0] >> (64 - lz) );
    exponent += (64 - lz);
  }
  else if( long_mantissa[0] != 0 )   // low half
  {
    unsigned int  lz = count_leading_zeros( long_mantissa[0] );
    mantissa = ( long_mantissa[0] << lz );
    exponent -= lz;
  }
  else
    return  0;   // product is unexpectedly zero

  // 3. Round mantissa
  ng_uint64_t  remainder = mantissa & UINT64_C(0x07FF);  // 0x07FF: 11 least significant bits set, where 11 = 64-53
  if( remainder < UINT64_C(0x0400) )
    mantissa -= remainder;
  else
  {
    mantissa += (UINT64_C(0x0800) - remainder);
    if( mantissa == 0 )
    {
      // handle overflow of the mantissa
      mantissa = (UINT64_C(1) << 63);
      ++exponent;
    }
  }

  // 4. Move binary point 63 bits to the left: adjust exponent
  exponent += 63;

  // 5. Save computation results and exit
  (*c) = mantissa;
  (*d) = exponent;
  return  1;
}

/**
*
*  Determine if extended-precision decimal rounds to given double-precision binary with 53-bit mantissa
*
*                                                             rounding
*  @returns  1  if there's a match in conversion  (a * 10^b) ----------> (expected_c * 2^expected_d)
*
*  @returns  0  if there's no match or computation error
*
*/
static int  does_extended_decimal_round_to_given_binary(
  ng_uint64_t   a,
  ng_int32_t    b,
  ng_uint64_t   expected_c,
  ng_int32_t    expected_d
)
{
  // 1. Check/normalize binary (expected_c * 2^expected_d)
  if( expected_c == 0 )
    return  0;
  unsigned int  lz = count_leading_zeros( expected_c );
  expected_c <<= lz;
  expected_d  -= lz;

  // 2. Convert (a * 10^b) to binary with rounding to 53 bits (in round-to-nearest mode)
  ng_uint64_t  actual_c = 0;
  ng_int32_t   actual_d = 0;
  if(! convert_extended_decimal_to_binary_and_round( a, b, &actual_c, &actual_d ) )
    return  0;

  // 3. Compare and compute result of this function
  return(( actual_c == expected_c )&&( actual_d == expected_d ));
}

/**
*
*  Convert extended-precision binary to extended-precision decimal (first approximation)
*
*  (a * 2^b) -> (c * 10^d)
*
*  a != 0. Binary point is after the most significant bit of the input mantissa a.
*
*  Resulting decimal mantissa is in the range 10^18 <= c < 10^19.
*  Decimal point is on the right side of resulting decimal mantissa c.
*
*  @returns  1  Exited normally, no errors.
*            0  Invalid argument or internal error.
*
*/
static int  convert_binary_to_decimal_1st_approx(
  ng_uint64_t   a,
  ng_int32_t    b,
  ng_uint64_t*  c,
  ng_int32_t*   d
)
{
  // 1. Check/normalize input mantissa.
  if( a == 0 )
    return  0;
  unsigned int  lz = count_leading_zeros( a );
  a <<= lz;
  b  -= lz;
 
  // 2. Move binary point to the right side of mantissa.
  //    Do bounds check of the input exponent.
  b -= 63;
  if(( b < powers_of_two_[0].binary_exponent )||
    ( b > powers_of_two_[sizeof(powers_of_two_)/sizeof(powers_of_two_[0]) - 1].binary_exponent ))
    return  0;
 
  // 3. Convert (a * 2^b) -> (long_mantissa * 10^exponent)
  ng_uint64_t  mantissa = powers_of_two_[ b - powers_of_two_[0].binary_exponent ].decimal_mantissa;
  ng_int32_t   exponent = powers_of_two_[ b - powers_of_two_[0].binary_exponent ].decimal_exponent;
  ng_uint64_t  long_mantissa[2];
  multiply_128( a, mantissa, long_mantissa );
      // invariant: 2^63*(0.1*2^64) < long_mantissa < 2^64*(1*2^64)
  mantissa = long_mantissa[1];
      // invariant: 2^63*0.1 < mantissa < 2^64
 
  // 4. Force resulting decimal mantissa into the range 10^18 <= c < 10^19.
  if( mantissa < POW10_18 )
  {
    mantissa *= UINT64_C(10);
    multiply_128( UINT64_C(10), long_mantissa[0], long_mantissa );
    mantissa += long_mantissa[1];
    --exponent;
  }
  else if( mantissa >= (UINT64_C(10) * POW10_18) )
  {
    mantissa /= UINT64_C(10);
    ++exponent;
  }
 
  // 5. Save computation results and exit
  (*c) = mantissa;
  (*d) = exponent;
  return  1;
}

/**
*
*  Convert double-precision binary to extended-precision decimal
*
*  (a * 2^b) -> (c * 10^d)
*
*  a != 0. Binary point is after the most significant bit of the input mantissa a.
*
*  Advantages of this function over convert_binary_to_decimal_1st_approx():
*  - this function works with inexact 53-bit mantissa,
*    result of bit uncompression of IEEE 754 double precision binary
*  - this function aims to provide minimal length of decimal representation
*    by zeroing out decimal digits known to be computed inexactly.
*
*  Resulting decimal mantissa is in the range 10^18 <= c < 10^19.
*  Decimal point is on the right side of resulting decimal mantissa c.
*
*  @returns  1  Exited normally, no errors.
*            0  Invalid argument or internal error.
*
*/
static int  convert_binary_to_extended_decimal(
  ng_uint64_t   a,
  ng_int32_t    b,
  ng_uint64_t*  c,
  ng_int32_t*   d
)
{
  // 1. Check input parameters and convert binary mantissa in such way that
  //    its most significant bits are 01. Zero out least significant bits of
  //    mantissa, which are not representable in IEEE 754 double precision format.
  if( a == 0 )
    return  0;
  unsigned int  lz = count_leading_zeros( a );
  a <<= lz;
  a >>= 1;
  b   = b - lz + 1;
  a &= (~ UINT64_C(0x03FF));    // 2 msb const `01' + 52 bits of mantissa + 10 lsb zeroed out = 64 bits total

  // 2. Compute first approximation for (a * 2^b), also for ((a-1) * 2^b) and ((a+1) * 2^b).
  //    Reduce all of them to the common exponent (base_d).
  ng_uint64_t  prev_c = 0;  ng_int32_t  prev_d = 0;
  ng_uint64_t  base_c = 0;  ng_int32_t  base_d = 0;
  ng_uint64_t  next_c = 0;  ng_int32_t  next_d = 0;
  if((! convert_binary_to_decimal_1st_approx( a - UINT64_C(0x0400), b, &prev_c, &prev_d ) )||
     (! convert_binary_to_decimal_1st_approx( a                   , b, &base_c, &base_d ) )||
     (! convert_binary_to_decimal_1st_approx( a + UINT64_C(0x0400), b, &next_c, &next_d ) ))
    return  0;
  if( prev_d < next_d )
  {
    prev_c /= UINT64_C(10);
    ++prev_d;
  }
  if( base_d < next_d )
  {
    base_c /= UINT64_C(10);
    ++base_d;
  }

  // 3. Check invariants before starting the binary search
  if(( prev_c >= base_c )||( base_c >= next_c )||
    (! does_extended_decimal_round_to_given_binary( prev_c, prev_d, a - UINT64_C(0x0400), b ) )||
    (! does_extended_decimal_round_to_given_binary( base_c, base_d, a                   , b ) )||
    (! does_extended_decimal_round_to_given_binary( next_c, next_d, a + UINT64_C(0x0400), b ) ))
     return  0;

  // 4. Using binary search, compute range of attraction: range of decimals
  //    around (base_c * 10^base_d) such that every number within it is mapped to 
  //    (a * 2^b) using convert_extended_decimal_to_binary_and_round().
  //    (prev_c * 10^base_d) and (next_c * 10^base_d) are known to be outside 
  //    this range of attraction, so lower bound is somewhere between prev_c and base_c
  //    and upper bound is somewhere between base_c and next_c.
  ng_uint64_t  negative_extent = 0, negative_search_space = base_c - prev_c;
  ng_uint64_t  positive_extent = 0, positive_search_space = next_c - base_c;
  for( ng_uint64_t  bit = UINT64_C(1) << (63 - count_leading_zeros( negative_search_space )); bit != 0; bit >>= 1 )
  {
   if(( bit <= negative_search_space )&&
      ( does_extended_decimal_round_to_given_binary( base_c - negative_extent - bit,
                                                     base_d, a, b ) ))
   {
     negative_extent       += bit;
     negative_search_space -= bit;
   }
  }
  for( ng_uint64_t  bit = UINT64_C(1) << (63 - count_leading_zeros( positive_search_space )); bit != 0; bit >>= 1 )
  {
    if(( bit <= positive_search_space )&&
       ( does_extended_decimal_round_to_given_binary( base_c + positive_extent + bit,
                                                      base_d, a, b ) ))
    {
      positive_extent       += bit;
      positive_search_space -= bit;
    }
  }

  // 5. Compute new decimal mantissa within range of attraction: digit after digit, most significant first
  ng_uint64_t  new_mantissa = 0;
  for( ng_uint64_t  current_scale = POW10_18; current_scale != 0; current_scale /= UINT64_C(10) )
  {
   ng_uint64_t  next_digit = ((base_c + positive_extent) / current_scale) % UINT64_C(10);
   new_mantissa += (current_scale * next_digit);
   if(( base_c - negative_extent <= new_mantissa )&&( new_mantissa <= base_c + positive_extent ))
     break;
  }

  // 6. Perform final normalization and offload results
  if( new_mantissa < POW10_18 )
  {
    new_mantissa *= UINT64_C(10);
    --base_d;
  }
  (*c) = new_mantissa;
  (*d) = base_d;
  return  1;
}

